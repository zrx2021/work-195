    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }

        if (obj == null) {
            return false;
        }

        if (getClass() != obj.getClass()) {
            return false;
        }

        Student stu = (Student) obj;
        if (age != stu.age) {
            return false;
        }

        if (!name.equals(stu.name)) {
            return false;
        }

        return true;
    }
